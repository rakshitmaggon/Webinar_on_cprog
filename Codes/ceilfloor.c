//Ceil and Floor
#include<stdio.h>
#include<math.h>
int main()
{
    double result_ceil=0;
    double result_floor=0;

    result_ceil=ceil(8.33);
    result_floor=floor(8.33);

    printf("The ceil value :%f\n",result_ceil);
    printf("The floor value :%f",result_floor);

    return 0;
}
